const withBasePath = (path: string) => {
  if (/^(https?:|blob:|data:)/.test(path)) return path;

  return `${import.meta.env.BASE_URL}${path.replace(/^\/+/, '')}`;
};

const getThumbnailPath = (path: string) => {
  if (/^(https?:|blob:|data:)/.test(path)) return path;

  return path.replace(/^\/references\//, '/references/thumbs/');
};

